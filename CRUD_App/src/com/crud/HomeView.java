    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked

        int row = table.getSelectedRow();

        if (row < 0) {
            JoptionPanel.showMessageDialog(null, "Por favor selecione qualquer resgtiro da tabela para atualizar");
        } else {
            id = (int) table.getValueAt(row, 0);
            HomeModel model = new HomeModel();
            Bean bean = model.retornarDados(id);
            fieldNomeCreateArea.setText(bean.getNome());
            fieldCpfCreateArea.setText(bean.getCpf());
            fieldTelefoneCreateArea.setText(bean.getTelefone());
            fieldEmailCreateArea.setText(bean.getEmail());
            fieldSenhaCreateArea.setText(bean.getSenha());
        }
    }//GEN-LAST:event_tableMouseClicked

