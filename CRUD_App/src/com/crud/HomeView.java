    public void carregarTabela(List<Bean> list) {
        DefaultTableModel tabela = (DefaultTableModel) table.getModel();
        tabela.setRowCount(0);

        for (Bean bean : list) {
            Vector vetor = new Vector();
            vetor.add(bean.getId());
            vetor.add(bean.getNome());
            vetor.add(bean.getCpf());
            vetor.add(bean.getTelefone());
            vetor.add(bean.getEmail());

            tabela.addRow(vetor);
        }
    }

    public void criarTabela() {
        HomeModel model = new HomeModel();
        List list = model.loadData();
        carregarTabela(list);
    }

